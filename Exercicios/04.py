"""
Exercício 3 — Salário diário
Um analista recebe R$3000 por mês. Considere 30 dias e calcule o valor diário
"""
salario = int(input("Insíra o valor do salário: "))
total_dias_trabalhados = 22
valor_diaria = salario / total_dias_trabalhados

print(f"O valor do salário é R$ {salario:.2f} e o valor da diária trabalhada é de R$ {valor_diaria:.2f}")